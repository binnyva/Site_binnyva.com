<h1>Assembling the PC</h1>

<p>After buying all the computer components they must be put together. This process is called assembling. It usually takes about fifteen minutes(installing not included). Although easy, it must be done carefully so as to avoid unnecessary damage to the system.</p>

<p>
<h3>Materials Required</h3>
Make sure that you have all the below materals before starting.
<ol>
	<li>All the necessary components(Although the all the below components are preferable, not all are necessary. Then necessary ones are marked with a *)
<blockquote>
- Processors *<br>
- Motherboard *<br>
- Hardisk *<br>
- RAM *<br>
- Cabinet *<br>
- Floppy Drive *<br>
- CD Drive *<br>
- <b>Cards</b><br>
 &nbsp; &nbsp; &nbsp; - Display Card (Not needed if On-board display is available on Motherboard)<br>
 &nbsp; &nbsp; &nbsp; - Sound Card (Not needed if On-board sound is available on Motherboard)<br>
 &nbsp; &nbsp; &nbsp; - Modem<br>
 &nbsp; &nbsp; &nbsp; - Other Cards(If Any)<br>
- Monitors *<br>
- Keyboard *<br>
- Mouse *<br>
- Speakers<br>
- UPS<br>
- Other Components(If Any)<br>
- Also keep the cables that came with the components close by
</blockquote></li>
	<li>Philips head Screwdriver (Or Star Screwdriver)
	<li>Flat head Screwdriver
	<li>Forceps (for pulling out jumpers and screws)
	<li>Magnatized Screwdriver
	<li>Multi meter (Testing)
</ol>
</p>

<p>
<h3>Required Environment</h3>

Make sure that the following things are true of the room in which the computer is assambled/kept.
<ul>
	<li>Make sure that a flat surface of a good area is available when the system is assembled. Make sure that the room has enough space to move.
	<li>See that the place where the Computer is kept is dust free as dust can harm the system.
	<li>Make sure that the room has good ventilation and won't be very hot.
	<li>Check the grounding in the plug to make sure that earthing is done properly.
</ul>
</p>

