<strong>Floppy Drive</strong><br />
<table><tr><td>
<img src="../../../images/assembling/connecting_fdd.jpg" width="300" height="225" alt="Connecting the Floppy Drives" />
</td><td class="td_text">
Ensure that one of the 3.5-inch bays in your case is open. If your case came with rails for the floppy drive, attach them to the sides of the drive and slide the drive into the front of the computer until it clicks into place. Rails are small metal pieces which clip or screw on to the sides of the drive and allow it to be inserted and removed from the case with minimal effort.<br />
Otherwise, slide the drive into the front of the computer until the faceplate of the floppy drive is flush with the front bezel of the case and the screw holes along the side of the drive line up with the case. When everything lines up, screw the floppy drive in securely on both sides.<br />
Plug in the power cable (see picture to left) carefully, since it is quite possible to miss one of the connectors, which will quite possibly cause some damage when the computer is powered on. Floppy drive power connectors are keyed in most cases, but if not, the red wire should be connected to the pin designated as 1 on the surrounding PCB.<br /> 
</td></tr></table>

