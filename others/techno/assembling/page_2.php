<p><h3>Precautions</h3>
Before Starting the actual assembly of the PC System, the following precautions would help you to avoid any mishap during the assembly process:
<ul>
<li>While the motherboard has to be fitted at a fixed place inside the PC cabinet, the locations of add-on cards (as and when used) and the drivers (hard disk drive, floppy disk drive, and CD-ROM drive) within the drivers bay of the cabinet can be changed within certain limits. But it is better to place them far away from each other. (The length of the cable provided for interconnections to the motherboard has to be taken into account, as there must be some slack after these are installed and connected.) This will improve the cooling and reduce the chance of electro-magnetic interference between them.
<li>The motherboard contains sensitive components, which can be easily damaged by static electricity. Therefore the motherboard should remain in its original anti static envelope until required for installation. The person taking it out should wear an anti static wrist strap that is properly grounded. In the absence of a proper wrist strap, you must make one on your own, using  a peeled of multi-stranded copper cable and ground it properly. Similar handling precaution are also required for cards.
<li>Be sure to handle all the components with great care. If a small thing like a screw is dropped on the MB, it can damage the delicate circuitry, rendering the MainBoard useless.
</ul></p>

