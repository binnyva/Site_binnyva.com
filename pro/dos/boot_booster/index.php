<h3>Introduction</h3>

<a class="bookmarks" name="1">What is Boot Booster?</a><br />
Boot Booster is a program that tries to put the booting time of the computer to good use. It can log the booting time, display a joke or an inspirational message that will change at every booting, display any message, display any reminders you have set etc. Boot Booster can  be configured to log the booting time, shutdown time and the time the session lasted etc.<br />
<a href="#top">Go To Top</a><br /><br />

<a class="bookmarks" name="2">What is it for?</a><br />
You can use the Boot Booster for a variety of tasks. I hate to wait while my computer <strong>slowly</strong> boot up. It takes about 77 seconds(average) to boot my computer. Boot Booster can be used to utilize this time. For example imagine that you have to memorize a chemical equation. You can set Boot Booster to display that equation every time the system boots. You will be forced to see the equation every time at booting. This is a sure fire way to memorize it. If you are not the studious type, you will be interested in the quotes function. This will show jokes that will change at every booting. You can set it to show different things. This can be famous quotes, inspirational words, study materials, Bible verses etc. For information on how to do this, see <a href="tricks.php">cool tricks section</a>. Another application is the logging system. If you want to monitor the use of your computer, you can enable the logging feature of Boot Booster. This will record every time the computer is booted to a file called "Record.txt"(File can be changed). This program will write the time and date of the booting of the computer to a file called "Record.txt" in this format <br />
<code>Session Started At : 31-12-1999  11:59 PM<br />
Session Ended At : 1-1-2000 1:00 AM<br />
Session Lasted 1 hours and 1 minutes.<br /></code>
It will also read one line from a file called "quotes.txt" and display it on the screen. I usually put one line jokes in this file so that I enjoy the booting time. If you suspect that your son/daughter is spending too much time with the computer, this is the best way to control it. The use of this feature is deadly when used in combination with the Shutdown logger and Session time logger. For more info see <a href="tricks.php">cool tricks section</a>.<br />
