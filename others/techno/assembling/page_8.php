</td></tr></table></p>

<h4>Connecting other peripherals</h4>
<p><table><tr><td>
<img src="../../../images/assembling/computer_back.jpg" width="266" height="200" alt="Back of the Cabinet" />
</td><td>
Now plug the data cable of the monitor to the display card. It will be marked on the back of the cabinet. If not, refer the manual to see where it is. Connect Keyboard, Speakers, Mouse to the back of the cabinet in the correct places. See that the Mouse is connected properly. If it is a PS/2 Mouse it must be connected to the PS/2 port. If it is a USB mouse, USB port must be used. Similarly for the COM mouse. Connect all other peripherals that you may have like Printer, External Modem, UPS, etc. to the back of the cabinet in their respective places. 
</td></tr></table></p>

<h4>Power Cables</h4>
<p>After all these connections, connect the power cable of the Monitor to its place. Then plug in the power cable into its jack. Give the other end to a plug nearby. If a UPS is available, plug the power cable to the UPS and the power cable of the UPS to the Power plug.</p>

<h4>Powering the System</h4>
<p>Plug in the power cord and switch the power supply switch to the on position. Press the power button. If everything is connected as it should be, all system fans should start spinning, you should hear a single beep, and after about 2-5 seconds the computer start to boot.<br /><br />
Depending on the manufacturer of the Motherboard, you may get a splash screen, or just a memory check. The system will then halt with an error because we have yet to install an operation system. Now check the front LEDs to see if you plugged them in correctly. Also test the reset button. Power off and fix the LEDS if there are any problems.</p>

<h4>Configurations</h4>
<p>At the first screen press the Delete key to enter the BIOS setup. Review the BIOS settings. Here are a few that you may want to pay attention to:
<ul>
    <li>Time and date.
    <li>IDE devices - Hardisk and Floppy Drive.
    <li>Boot Order.
</ul></p>

